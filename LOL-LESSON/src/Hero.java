public class Hero {
    private String Name,Hp,Mp,Atk,Speed;
    public void setName(String newName){
        this.Name=newName;
    }
    public String getName(){
        return this.Name;
    }
    public void setHp(String newHp){
        this.Hp=newHp;
    }
    public String getHp(){
        return this.Hp;
    }
    public void setMp(String newMp){
        this.Mp=newMp;
    }
    public String getMp(){
        return this.Mp;
    }
    public void setAtk(String newAtk){
        this.Atk=newAtk;
    }
    public String getAtk(){
        return this.Atk;
    }
    public void setSpeed(String newSpeed){
        this.Speed=newSpeed;
    }
    public String getSpeed(){
        return this.Speed;
    }
}
